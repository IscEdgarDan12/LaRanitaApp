    public boolean existenciaUsuarios() {
        String sql = "SELECT idUSuarios FROM usuarios WHERE usuario='" + usuario.getText() + "'";
        boolean encontro = false;
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if (rs != null) {
                System.out.println("entro aqui que debeira de ser flase");
              encontro = false;
            } else {

                encontro = true;
            }
        } catch (Exception e) {
            System.out.println("error " + e);
        }
        return encontro;
    }

    public void registro() {
        try {
            if (existenciaUsuarios()) {
                if (nombre.getText().equals("") || apellido.getText().equals("")
                        || usuario.getText().equals("")
                        || String.valueOf(contra.getPassword()).equals("")
                        || String.valueOf(email.getText()).equals("")) {
                    JOptionPane.showMessageDialog(null, "Registro no completado favor de llenar todos los campos");
                } else {
                    PreparedStatement pst = cn.prepareStatement("INSERT INTO usuarios "
                            + "(idUsuarios, nombre, apellido, usuario, pass, email,tipoUsuario) VALUES (?,?,?,?,?,?,?)");
                    pst.setString(1, idUsuario(usuario.getText()));
                    pst.setString(2, nombre.getText());
                    pst.setString(3, apellido.getText());
                    pst.setString(4, usuario.getText());
                    pst.setString(5, String.valueOf(contra.getPassword()));
                    pst.setString(6, email.getText());
                    pst.setString(7, "0");

                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Registro completado");
                    ventanaLogin();
                }

            } else {
                JOptionPane.showMessageDialog(null, "Usuario ya Existente");

            }

        } catch (Exception e) {
            System.out.println("Error a agregar un dato" + e.getMessage());
        }

    }

    public String idUsuario(String usuario) {
        Random random = new Random();
        SimpleDateFormat dtf = new SimpleDateFormat("ddmmyy");
        Calendar calendar = Calendar.getInstance();
        Date date = calendar.getTime();
        String fecha = dtf.format(date);
        String idiUsuario = "";
        if (usuario.length() >= 3) {
            for (int i = 0; i < 3; i++) {
                idiUsuario = idiUsuario + usuario.charAt(i);
            }
        } else {
            for (int i = 0; i < usuario.length(); i++) {
                idiUsuario = idiUsuario + usuario.charAt(i);
            }
        }
        idiUsuario = fecha + idiUsuario;
        String idusu="";
        for (int i = 0; i < idiUsuario.length(); i++) {
            idusu+=idiUsuario.charAt(random.nextInt(idiUsuario.length()));
        }
        return idusu;
    }

